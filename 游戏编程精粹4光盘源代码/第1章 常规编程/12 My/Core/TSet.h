namespace GPG4
{
  template <class CObj> class TSet
  {
  // typedefs

  public:

    typedef std::set< CObj >            setType;
    typedef setType::iterator           iterType;

  // public methods

  public:
                    TSet                (void);
                   ~TSet                (void);

    void            ClearSet            (void);
    void            AddToSet            (const CObj& Obj);
    bool            RemoveFromSet       (const CObj& Obj);
    bool            IsInSet             (const CObj& Obj);

  // protected data

  protected:

    setType         m_Set;
  };
}

