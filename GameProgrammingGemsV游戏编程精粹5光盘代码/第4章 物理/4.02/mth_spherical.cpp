bool mth_sph_to_cart(const float * sph, float * cart)
{
	if(!sph || !cart)
		return false;

	float temp = (float)(sph[2] * sin(sph[1]));

	cart[0] = (float)(temp * cos(sph[0]));
	cart[1] = (float)(temp * sin(sph[0]));
	cart[2] = (float)(sph[2] * cos(sph[1]));

	return true;
}
bool mth_cart_to_sph(float * sph, const float * cart)
{
	if(!sph || !cart)
		return false;

	float temp = (float)sqrt(cart[0]*cart[0] + cart[1]*cart[1]);
	if(!temp)
		sph[0] = 0.0f;
	else
		sph[0] = (float)asin(cart[1]/temp);

	sph[2] = (float)sqrt(cart[0]*cart[0] + cart[1]*cart[1] + cart[2]*cart[2]);

	if(!sph[2])
		sph[1] = 0.0f;
	else
		sph[1] = (float)acos(cart[2]/sph[2]);

	sph[0] = mth_reduce_rad(sph[0]);
	sph[1] = mth_reduce_rad(sph[1]);

	return true;
}

